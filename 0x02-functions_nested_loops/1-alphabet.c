#include "main.h"

/**
 * main - Entry point
 *
 * print_alphabet - Prints alphabets in lowercase followed by a new line
 *
 * Return: Always 0
 */
void print_alphabet(void)
{
	char c;

	for (c = 'a'; c <= 'z'; c++)
	{
		_putchar('c');
	}
	_putchar('\n');
}

int main(void)
{
	print_alphabet();
	return (0);
}
